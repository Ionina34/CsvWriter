package org.writer.model;

/**
 * Перечисление {@code Months} представляет месяцы года. Используется для указания месяца рождения
 * в моделях данных, таких как {@link org.writer.model.Person}.
 *
 * <p>Содержит все 12 месяцев года в порядке их следования.</p>
 */
public enum Months {
    JANUARY,
    FEBRUARY,
    MARCH,
    APRIL,
    MAY,
    JUNE,
    JULY,
    AUGUST,
    SEPTEMBER,
    OCTOBER,
    NOVEMBER,
    DECEMBER
}